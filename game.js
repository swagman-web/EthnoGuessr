// Extract image names (values) from the dictionary
const images = Object.values(Countries);
// Shuffle function to randomize the image array
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

// Game variables
let currentRound = 0;            // Track the current round
let totalRounds = 10;            // Number of rounds to play
let score = 0;                   // Track the player's score
let shuffledImages = [];         // Will hold the shuffled array of images
let imageName = '';              // The name of the current image without extension

// Select elements from the DOM
const imageDisplay = document.getElementById('imageDisplay');
const guessInput = document.getElementById('guessInput');
const result = document.getElementById('result');

// Start the game by loading the first image
function startGame() {
    currentRound = 0;
    score = 0;
    shuffledImages = shuffleArray(images.slice());  // Shuffle the image values for non-repeating rounds
    loadNextImage();                                // Load the first image
}

// Load the next image in the shuffled array
function loadNextImage() {
    if (currentRound < totalRounds) {
        imageName = shuffledImages[currentRound];  // Get the next image name from the shuffled array
        loadImageWithFallback(imageName);          // Load the image
        currentRound++;
    } else {
        endGame();  // End the game after the set number of rounds
    }
}

// Function to load an image and fallback to .png if .jpg fails
function loadImageWithFallback(imageName) {
    const image = new Image();  // Create a new Image object

    let tall = Math.floor(Math.random()*5)+1
    let manwomchi =["m", "w", "c"] 
    let rando = Math.floor(Math.random()* manwomchi.length)
    let mwc = manwomchi[rando]  

    console.log(mwc)

    // Try loading the .jpg version first
    image.src = `${imageName}_${mwc}/00000${tall}.jpg`;

    // If .jpg loads successfully, display it
    image.onload = function() {
        imageDisplay.innerHTML = `<img src="${imageName}_${mwc}/00000${tall}.jpg" alt="${imageName}">`;
        guessInput.value = '';         // Clear the input field for the next guess
        result.textContent = '';       // Clear previous result
        result.style.color = 'black';  // Reset text color
    };

    // If .jpg fails to load, try loading .png
    image.onerror = function() {
        console.log(`${imageName}.jpg failed to load. Trying ${imageName}.png`);
        image.src = `${imageName}_${mwc}/00000${tall}.png`;

        // If .png loads successfully, display it
        image.onload = function() {
            imageDisplay.innerHTML = `<img src="${imageName}_${mwc}/00000${tall}.png" alt="${imageName}">`;
            guessInput.value = '';         // Clear the input field for the next guess
            result.textContent = '';       // Clear previous result
            result.style.color = 'black';  // Reset text color
        };

        // If both .jpg and .png fail to load
        image.onerror = function() {
            console.log(`Both ${imageName}.jpg and ${imageName}.png failed to load.`);
            imageDisplay.innerHTML = `<p>Image not found</p>`;
            guessInput.disabled = true;  // Disable guessing if image fails
            result.textContent = 'Image not found. Skipping to next image.';
            result.style.color = 'red';

            // Skip to the next image after a short delay
            setTimeout(loadNextImage, 1500);
        };
    };
}

// Check the player's guess
function checkGuess() {
    const guess = guessInput.value.trim().toLowerCase();
    if (guess === imageName.toLowerCase()) {
        result.textContent = "Correct! You guessed the Country.";
        result.style.color = "green";
        score++;  // Increase the score for a correct guess
    } else {
        result.textContent = `Wrong! The correct name was: "${imageName}"`;
        result.style.color = "red";
    }

    // After checking the guess, load the next image after a short delay
    setTimeout(loadNextImage, 1500);  // Load next image after 1.5 seconds
}

// End the game and display the total score
function endGame() {
    imageDisplay.innerHTML = '';  // Clear the image display
    result.innerHTML = `Game over! Your total score is: ${score}/${totalRounds}`;
    result.innerHTML += `\n`
    result.innerHTML += `<button onClick="window.location.reload();">Play again</button>`
    result.style.color = "blue";
    guessInput.disabled = true;   // Disable the input field after the game ends
}

// Start the game when the page loads
window.onload = startGame;
